//----------------------------------------(ABRIR A PORTA)-----------------------------------------------
void Abrir_Porta(void){
	if (HCSR04_ReadDistance() <= 20){
		Servo_SetAngle(90);
		if (estado_B == 0){
		Buzzer_On();
		estado_B = 1;
		}
		HAL_Delay(500);
		Buzzer_Off();
	}else{
		HAL_Delay(2000);
		Servo_SetAngle(0);
		estado_B = 0;
	}
}



